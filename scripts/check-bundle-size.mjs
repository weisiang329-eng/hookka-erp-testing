#!/usr/bin/env node
// ---------------------------------------------------------------------------
// Bundle size budget gate (P7.2).
//
// Reads dist/assets/*.js after a `vite build`, groups chunks by their
// post-build "stem" (the filename minus the trailing `-<hash>.js` segment),
// and compares each stem's total size against `.bundle-baseline.json`.
// Fails (exit 1) if any stem grew by more than the threshold.
//
// Why "stem" instead of full filename: Vite emits content-hashed filenames
// (`react-vendor-t35I75rJ.js`) that change every build. We can't pin the
// baseline to the hash. We CAN pin it to the human-readable stem the
// rolldown chunking config produced. If two chunks share a stem (e.g. a
// route called "bom" imports a vendor lib also named "bom"), we sum them —
// the baseline tracks total per-stem footprint, not per-file.
//
// Usage:
//   node scripts/check-bundle-size.mjs                   # gate mode
//   node scripts/check-bundle-size.mjs --write-baseline  # snapshot mode
//
// CI wires this in as a `continue-on-error: true` step right after the
// build. Once we have a clean signal week, flip it to blocking — same
// graduation pattern P1.2 used for typecheck.
// ---------------------------------------------------------------------------
import { readdir, stat, readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const ASSETS_DIR = join(REPO_ROOT, "dist", "assets");
const BASELINE_PATH = join(REPO_ROOT, ".bundle-baseline.json");

// 5% growth threshold. Anything larger fails the gate. Tuned to allow
// normal feature work (a few extra imports, a few KB of icons) without
// constant baseline updates, while catching the "I imported all of lodash"
// class of regression.
const GROWTH_THRESHOLD = 0.05;

// Filenames look like `<stem>-<hash>.js`. Vite/rolldown's content hash is
// exactly 8 base64url characters — so the hash itself can contain `-` or
// `_`, e.g. `POD-dialog-QP-ltr3-.js` (stem=POD-dialog, hash=QP-ltr3-).
// The regex is anchored greedy: `^(.+)-([8 chars])\.js$` — the greedy
// `.+` consumes as much as possible, leaving exactly 8 chars + the dash
// separator at the tail. Earlier dashes inside the stem are preserved.
// Files that don't match (unhashed entry chunks) fall back to the full
// filename minus `.js`.
const STEM_RE = /^(.+)-([A-Za-z0-9_-]{8})\.js$/;

function stemOf(filename) {
  const m = filename.match(STEM_RE);
  if (m) return m[1];
  return filename.replace(/\.js$/, "");
}

async function collectStems() {
  let entries;
  try {
    entries = await readdir(ASSETS_DIR);
  } catch (err) {
    console.error(
      `[bundle-size] dist/assets not found at ${ASSETS_DIR} — did you run \`npm run build\`?`,
    );
    console.error(err.message);
    process.exit(2);
  }

  // Map<stem, totalBytes>. Sum-by-stem so two files sharing a stem don't
  // confuse the diff (Vite reshuffling which physical file gets which
  // chunk shouldn't trigger a regression).
  const totals = new Map();
  for (const name of entries) {
    if (!name.endsWith(".js")) continue;
    const full = join(ASSETS_DIR, name);
    const s = await stat(full);
    if (!s.isFile()) continue;
    const stem = stemOf(name);
    totals.set(stem, (totals.get(stem) ?? 0) + s.size);
  }
  return totals;
}

function formatKB(bytes) {
  return (bytes / 1024).toFixed(2) + " KB";
}

async function writeBaseline(totals) {
  // Stable order — alphabetical by stem — so the JSON diff in PRs is
  // readable when the baseline is intentionally bumped.
  const obj = {};
  for (const stem of [...totals.keys()].sort()) {
    obj[stem] = totals.get(stem);
  }
  const payload = {
    generatedAt: new Date().toISOString(),
    threshold: GROWTH_THRESHOLD,
    note:
      "Generated by scripts/check-bundle-size.mjs --write-baseline. Sizes are bytes per chunk-stem (Vite hashed filenames stripped). To intentionally bump after an approved increase, regenerate.",
    chunks: obj,
  };
  await writeFile(
    BASELINE_PATH,
    JSON.stringify(payload, null, 2) + "\n",
    "utf8",
  );
  console.log(
    `[bundle-size] Wrote baseline: ${BASELINE_PATH} (${totals.size} stems)`,
  );
}

async function loadBaseline() {
  try {
    const txt = await readFile(BASELINE_PATH, "utf8");
    const parsed = JSON.parse(txt);
    if (!parsed || typeof parsed.chunks !== "object") {
      throw new Error("baseline missing `chunks` object");
    }
    return parsed;
  } catch (err) {
    console.error(`[bundle-size] Failed to read baseline ${BASELINE_PATH}.`);
    console.error(err.message);
    console.error(
      "[bundle-size] Run `node scripts/check-bundle-size.mjs --write-baseline` to generate one.",
    );
    process.exit(2);
  }
}

async function gate() {
  const totals = await collectStems();
  if (totals.size === 0) {
    console.error("[bundle-size] No JS chunks found in dist/assets — abort.");
    process.exit(2);
  }

  const baseline = await loadBaseline();
  const baselineChunks = baseline.chunks;
  const threshold = baseline.threshold ?? GROWTH_THRESHOLD;

  const regressions = [];
  const newChunks = [];
  for (const [stem, current] of totals) {
    const before = baselineChunks[stem];
    if (before === undefined) {
      newChunks.push({ stem, current });
      continue;
    }
    if (before === 0) continue; // avoid div-by-zero on a 0-byte baseline
    const growth = (current - before) / before;
    if (growth > threshold) {
      regressions.push({ stem, before, current, growth });
    }
  }

  // New chunks aren't an automatic failure — they often come from new
  // routes / lazy splits — but we surface them so the reviewer notices.
  if (newChunks.length > 0) {
    console.log(
      `[bundle-size] ${newChunks.length} new chunk stem(s) (not in baseline):`,
    );
    for (const c of newChunks.sort((a, b) => b.current - a.current)) {
      console.log(`  + ${c.stem}: ${formatKB(c.current)}`);
    }
  }

  // Removed stems — also informational. A chunk going away usually means
  // a route was deleted or a vendor was split out under a different name.
  const removed = [];
  for (const stem of Object.keys(baselineChunks)) {
    if (!totals.has(stem)) removed.push(stem);
  }
  if (removed.length > 0) {
    console.log(
      `[bundle-size] ${removed.length} stem(s) in baseline but not in current build:`,
    );
    for (const s of removed.sort()) {
      console.log(`  - ${s}: ${formatKB(baselineChunks[s])}`);
    }
  }

  if (regressions.length === 0) {
    console.log(
      `[bundle-size] OK — all ${totals.size} chunk stems within +${(threshold * 100).toFixed(0)}% of baseline.`,
    );
    return;
  }

  console.error(
    `[bundle-size] FAIL — ${regressions.length} chunk stem(s) grew more than ${(threshold * 100).toFixed(0)}%:`,
  );
  for (const r of regressions.sort((a, b) => b.growth - a.growth)) {
    console.error(
      `  ${r.stem}: ${formatKB(r.before)} -> ${formatKB(r.current)} (+${(r.growth * 100).toFixed(1)}%)`,
    );
  }
  console.error(
    "\n[bundle-size] To intentionally accept the new sizes, regenerate the baseline:",
  );
  console.error(
    "  node scripts/check-bundle-size.mjs --write-baseline",
  );
  console.error(
    "and commit `.bundle-baseline.json` alongside the change.",
  );
  process.exit(1);
}

const args = new Set(process.argv.slice(2));
if (args.has("--write-baseline")) {
  const totals = await collectStems();
  if (totals.size === 0) {
    console.error("[bundle-size] No JS chunks found — abort.");
    process.exit(2);
  }
  await writeBaseline(totals);
} else {
  await gate();
}
